// The centile lines shown on the NHS Red Book (UK-WHO) charts, with z-scores.
export const RED_BOOK_CENTILES: { label: string; z: number }[] = [
  { label: '2nd', z: -2.05375 },
  { label: '9th', z: -1.34076 },
  { label: '25th', z: -0.67449 },
  { label: '50th', z: 0 },
  { label: '75th', z: 0.67449 },
  { label: '91st', z: 1.34076 },
  { label: '98th', z: 2.05375 },
]
export const MEDIAN_INDEX = 3 // index of the 50th centile above

function valueFromLMS([l, m, s]: LMS, z: number): number {
  return l === 0 ? m * Math.exp(s * z) : m * Math.pow(1 + l * s * z, 1 / l)
}

export interface CentileSeries {
  label: string
  byMonth: number[] // value at each month 0..24
}

export function centileSeries(sex: Sex, measure: Measure): CentileSeries[] {
  const table = TABLES[measure][sex]
  return RED_BOOK_CENTILES.map((c) => ({
    label: c.label,
    byMonth: table.map((lms) => valueFromLMS(lms, c.z)),
  }))
}
